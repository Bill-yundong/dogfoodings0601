export const getMaterialById = (id: string): MaterialData | undefined => {
  return materialDatabase.find((material) => material.id === id);
};

export const getMaterialsByCategory = (
  category: MaterialData['category'],
): MaterialData[] => {
  return materialDatabase.filter((material) => material.category === category);
};

export const getMaterialsBySeason = (
  season: 'spring' | 'summer' | 'autumn' | 'winter',
): MaterialData[] => {
  return materialDatabase.filter((material) => material.seasonality.includes(season));
};

export const getMaterialsByOccasion = (occasion: string): MaterialData[] => {
  return materialDatabase.filter((material) => material.occasions.includes(occasion));
};
